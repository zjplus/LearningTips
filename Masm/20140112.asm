DATAS SEGMENT
	MEM DW 100 DUP(?)
DATAS ENDS

STACKS SEGMENT
	DB 8 DUP(0)
STACKS ENDS

CODES SEGMENT
	ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
	MOV AX,DATAS
	MOV DS,AX
	
BEGIN:
	MOV SI,198			;指向最后一个字
	MOV BX,-2
	MOV CX,100
L:  ADD BX,2
	CMP MEM[BX],0
	JZ ZERO				;是0就跳到ZERO
	LOOP L
	JMP OVER			;再找不到0就跳出（最后一步）
	
ZERO:
	MOV DI,BX			
	MOV AX,[DI+2]		;从0这个数开始，后面每一位数向前移一位
	MOV [DI],AX
	ADD DI,2
	CMP DI,200			;到了字串末，跳到L1
	JZ L1
	JMP ZERO
	
L1: MOV WORD PTR [SI],0 ;把最末位的字置0
	JMP BEGIN			;再从头开始找0

OVER:	
	MOV AH,4CH
	INT 21H
CODES ENDS
END START
