DATAS SEGMENT
    ;此处输入数据段代码
    STR1 DB 'Please input the string:$'
    STR2 DB 'The sum of num is:$'
    CRLF DB 0AH,0DH,'$'
DATAS ENDS

STACKS SEGMENT
    DB 8 DUP(0)
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
    ;此处输入代码段代码
    MOV DX,OFFSET STR1
    MOV AH,09H
    INT 21H
    MOV CL,0
INPUT:
	MOV AH,01H
	INT 21H
	CMP AL,24H
	JZ OUTPUT
	CMP AL,30H
	JL G
	CMP AL,39H
	JNBE G
	JMP INPUT
G:
	INC CL
	JMP INPUT
	
OUTPUT:	
	MOV DX,OFFSET CRLF
    MOV AH,09H
    INT 21H
	MOV DX,OFFSET STR2
    MOV AH,09H
    INT 21H
    CMP CL,9
    JNL NEXT 
    MOV DL,CL
    ADD DL,30H
	MOV AH,02H
	INT 21H
	JMP OVER
NEXT:
	MOV AH,0
	MOV AL,CL
	MOV BL,10
	DIV BL
	PUSH AX
	MOV DL,AL
	ADD DL,30H
	MOV AH,02H
	INT 21H
	POP AX
	MOV DL,AH
	ADD DL,30H
	MOV AH,02H
	INT 21H
OVER:
    MOV AH,4CH
    INT 21H
CODES ENDS
    END START
